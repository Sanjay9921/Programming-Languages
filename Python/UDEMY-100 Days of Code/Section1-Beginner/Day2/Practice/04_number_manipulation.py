# 4. Number manipulation and precision in Python

## 4.1 round() function

print(round(3.14)) # floor
print(round(3.64)) # ceil

## 4.2 Precision vs Floor Division

print(round(22/7,3)) # precision
print(22//7) # floor division

## 4.3 f-Strings

name = "Ryan Gosling"
print(f"I am literally {name} from Drive 2011.")