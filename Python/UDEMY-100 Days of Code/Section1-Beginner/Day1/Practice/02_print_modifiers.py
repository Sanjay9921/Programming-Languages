# 2. Print Modifiers

## 2.1 Errors

# print('I am: 'Ryan Gosling' from Drive 2011.')
# print("I am: "Ryan Gosling" from Drive 2011.")

## 2.2 Possible Ways

### 2.2.1 Single quotes inside double
print("I am: 'Ryan Gosling' from Drive 2011.")

### 2.2.2 Double quotes inside single
print('I am: "Ryan Gosling" from Drive 2011.')

### 2.2.3 Escape characters - works with same quotes
print("I am: \"Ryan Gosling\" from Drive 2011.")

## 2.3 Best Practices
print(input("What is your name?")) #❌
print(input()) #✅